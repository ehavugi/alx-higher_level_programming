#!/usr/bin/python3
""" check http code and if greater than 400 print "Error code: xxx" else print content """
import requests
import sys

if __name__ == "__main__":
    url = sys.argv[1]
    response = requests.get(url)
    if response.status_code >= 400:
        print("Error code: {}".format(response.status_code))
    else:
        print(response.text)
